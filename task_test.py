import io
import unittest
import unittest.mock

from task import print_logo

class TestTask(unittest.TestCase):
    @unittest.mock.patch('sys.stdout', new_callable=io.StringIO)
    def assert_stdout(self, n, expected_output, mock_stdout):
        print_logo(n)
        self.assertEqual(mock_stdout.getvalue(), expected_output)

    def test_task_constraint(self):
        # N > 2
        self.assertEqual(print_logo(1), False)
        # N < 10000
        self.assertEqual(print_logo(10001), False)
        # odd number
        self.assertEqual(print_logo(4), False)

    def test_task_N3(self):
        self.assert_stdout(3,
            '---***---***------***---***---\n'
            '--*****-*****----*****-*****--\n'
            '-***-*****-***--***-*****-***-\n'
            '***---***---******---***---***\n')

    def test_task_N5(self):
        self.assert_stdout(5,
            '-----*****-----*****----------*****-----*****-----\n'
            '----*******---*******--------*******---*******----\n'
            '---*********-*********------*********-*********---\n'
            '--*****-*********-*****----*****-*********-*****--\n'
            '-*****---*******---*****--*****---*******---*****-\n'
            '*****-----*****-----**********-----*****-----*****\n')

    def test_task_N7(self):
        self.assert_stdout(7,
            '-------*******-------*******--------------*******-------*******-------\n'
            '------*********-----*********------------*********-----*********------\n'
            '-----***********---***********----------***********---***********-----\n'
            '----*************-*************--------*************-*************----\n'
            '---*******-*************-*******------*******-*************-*******---\n'
            '--*******---***********---*******----*******---***********---*******--\n'
            '-*******-----*********-----*******--*******-----*********-----*******-\n'
            '*******-------*******-------**************-------*******-------*******\n')


if __name__ == '__main__':
    unittest.main()